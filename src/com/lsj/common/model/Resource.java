package com.lsj.common.model;

public class Resource {
	
	private Integer sid;
	private String name;
	private String url;
	private Integer cksPower;
	private String dispalyStatus;
	private String enableStatus;
	private String remarks;
	
	public Integer getSid() {
		return sid;
	}
	public void setSid(Integer sid) {
		this.sid = sid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public Integer getCksPower() {
		return cksPower;
	}
	public void setCksPower(Integer cksPower) {
		this.cksPower = cksPower;
	}
	public String getDispalyStatus() {
		return dispalyStatus;
	}
	public void setDispalyStatus(String dispalyStatus) {
		this.dispalyStatus = dispalyStatus;
	}
	public String getEnableStatus() {
		return enableStatus;
	}
	public void setEnableStatus(String enableStatus) {
		this.enableStatus = enableStatus;
	}
	public String getRemarks() {
		return remarks;
	}
	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}
	

}
